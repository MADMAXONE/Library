    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       String categorii = categorie_txt.getText();
       String status = status_txt.getSelectedItem().toString();
       
       
        try {
            pst = con.prepareStatement("insert into categorii(`Nume Categorie`,`Status`)values(?,?)");
            pst.setString(1, categorii);
            pst.setString(2, status);
            int k = pst.executeUpdate();
            
            
            if(k== 1)
            {
                JOptionPane.showMessageDialog(this,"Categorie Creata");
                
                categorie_txt.setText("");
                 status_txt.setSelectedIndex(-1);
                 categorie_txt.requestFocus();
                 Categorii_Load();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Eroare");  
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Categorii.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

