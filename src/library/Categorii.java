    // Incarca datele din tabel
    public void Categorii_Load()
    {
        int c;
        
        try {
            pst = con.prepareStatement("select * from Categorii");// pregateste statementul SQL
            rs = pst.executeQuery();// executa query-ul
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount(); // numarul de coloane
            
             // seteaza modelul tabelului
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            // adauga datele in tabel
            while (rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i = 1; i<=c; i++)
                {
                    v2.add(rs.getString("ID"));
                    v2.add(rs.getString("Nume Categorie"));
                    v2.add(rs.getString("Status"));
                }   
                
                d.addRow(v2);
                
                
                
            }
            
           
        } catch (SQLException ex) {
            Logger.getLogger(Categorii.class.getName()).log(Level.SEVERE, null, ex);
        }
         
    }    
    
    
    

